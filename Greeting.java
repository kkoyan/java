class Greeting{//クラス名とファイル名は同じにしないといけない。クラス名は大文字。{}はブロック。命令文(処理)を記載。
    public static void main(String args[]){//メソッド。どんな処理かを記載。
        System.out.println("Good morning");//処理の終わりに;を入力する決まり。
        System.out.println("Good afternoon");//コンパイルとはJAVAのプログラムをPCが読み取れる機械語に変換すること。（javac ファイル名）
        System.out.println("Good evening");
    }
}